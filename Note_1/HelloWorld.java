/* 类名要和文件名保持一致，文件名后缀为.java */
/* 大小写敏感                                */ 
/* 类名首字母大写,每个单词首字母大写         */
public class HelloWorld 
{
    /* main是主方法，所有java程序由该主方法进入 */
    /* 所有方法首字母小写，后续单词首字母大写   */
    public static void main(String []args)
    {
        System.out.println("Hello World");    /* main中的函数内容，打印hello world，类似printf */
    }
} 
