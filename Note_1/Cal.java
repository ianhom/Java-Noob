public class Cal{
    public static void main(String args[]){
        int a = 20;
        int b = 10;
        int c = 4;
        int d = 5;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % c = " + (a % c));
        System.out.println("b % c = " + (b % c));
        System.out.println("d++ = " + (d++));
        System.out.println("d-- = " + (d--));
        System.out.println("++d = " + (++d));
    }
}
