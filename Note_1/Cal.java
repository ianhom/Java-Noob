public class Cal{     /* 类名 */
    public static void main(String args[]){    /* 主方法 */
        /* 临时变量 */
        int a = 20;    
        int b = 10;
        int c = 4;
        int d = 5;
        
        /* 打印运算结果 */
        /* 算术运算 */
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % c = " + (a % c));
        System.out.println("b % c = " + (b % c));
        System.out.println("d++ = " + (d++));
        System.out.println("d-- = " + (d--));
        System.out.println("++d = " + (++d));
        
        /* 关系运算 */
        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("a >= b = " + (a >= b));
        System.out.println("a <= b = " + (a <= b));
        
    }
}
/* 普通运算基本与c类似 */
